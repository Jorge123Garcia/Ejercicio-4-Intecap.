package com.mycompany.ejercicioconforeachtarea4;


public class EjercicioConForEachTarea4 {

    public static void main(String[] args) {
        String[] nombres = {"Julio", "Cristian", "Noemi", "Jose", "Monica", "Roberto", "Ivan"};
        
        for(String i: nombres){
            System.out.println("Nombres: "+i);}
    }
}
